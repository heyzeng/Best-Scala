package practice02;


public class RelationOper {

    public static void main(String[] args) {


        System.out.println(10 > 3); //true
        System.out.println(10 < 3); // false
        System.out.println(10 == 3);// false
        System.out.println(10 != 3);// true

        int a = 10;
        int b = 4;
        boolean isOk = a < b;
        System.out.println("isOk=" + isOk); // false



    }
}
