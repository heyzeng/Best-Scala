package practice02;

public class IntDetail {
    public static void main(String[] args) {

        //Java的整型常量默认为 int 型，声明long型常量须后加‘l’或‘L’
        long n1 = 21474836L;

        //java程序中变量常声明为int型，除非不足以表示大数，才使用long
        //原则：尽量使用小的数据类型，可以节省空间.
        //long age = 100;

        byte n2 = 3;
        short n3 = 3;

    }
}
